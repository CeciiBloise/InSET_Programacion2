"""Utilizando la librería Math y la función pow probar que devuelve si le paso
como parámetro (12.5,2.8) y luego (144,0.5)"""

import math

numero1 = 12.5
numero2 = 2.8
numero3 = 144
numero4 = 0.5

#Calculo la potencia usando la función pow de math
resultado1 = math.pow(numero1, numero2)
resultado2 = math.pow(numero3, numero4)

print(resultado1)
print(resultado2)
