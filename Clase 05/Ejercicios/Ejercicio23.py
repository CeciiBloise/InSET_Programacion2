"""Utilizando la librería Math y la función trunc probar que devuelve si le paso
como parámetro (1.001) y (1.999)"""
import math

numero1 = 1.001
numero2 = 1.999

#Trunco los números usando la función trunc de math
resultado1 = math.trunc(numero1)
resultado2 = math.trunc(numero2)

print(resultado1)
print(resultado2)
